/**
 * Rebuild nepal-addresses.ts from complete official-style datasets:
 * - abinrimal/nepal-administrative-divisions-json (7 provinces, 77 districts, 753 local levels)
 * English display names keep type suffix so UI matches existing UX.
 * Wards: type-based full selectable ranges (covers max used in practice).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const tempDir = path.join(process.env.TEMP || "/tmp", "nepal-addr");
const adminPath = path.join(tempDir, "nepal_admin.json");

if (!fs.existsSync(adminPath)) {
  console.error("Missing", adminPath, "- download nepal_administrative_divisions.json first");
  process.exit(1);
}

const admin = JSON.parse(fs.readFileSync(adminPath, "utf8"));

/** Map source province names → app display names (backward compatible where possible). */
const provinceNameMap = {
  "Koshi Pradesh": { en: "Koshi Province", np: "कोशी प्रदेश" },
  "Madhesh Pradesh": { en: "Madhesh Province", np: "मधेश प्रदेश" },
  "Bagmati Pradesh": { en: "Bagmati Province", np: "बागमती प्रदेश" },
  "Gandaki Pradesh": { en: "Gandaki Province", np: "गण्डकी प्रदेश" },
  "Lumbini Pradesh": { en: "Lumbini Province", np: "लुम्बिनी प्रदेश" },
  "Karnali Pradesh": { en: "Karnali Province", np: "कर्णाली प्रदेश" },
  "Sudurpashchim Pradesh": { en: "Sudurpashchim Province", np: "सुदूरपश्चिम प्रदेश" }
};

/** Default ward counts by local level type (covers standard maxima for selection). */
const wardCountByType = {
  "Metropolitan City": 32,
  "Sub Metropolitan City": 19,
  "Sub-Metropolitan City": 19,
  Municipality: 14,
  "Rural Municipality": 9
};

const typeSuffixEn = {
  "Metropolitan City": "Metropolitan City",
  "Sub Metropolitan City": "Sub-Metropolitan City",
  "Sub-Metropolitan City": "Sub-Metropolitan City",
  Municipality: "Municipality",
  "Rural Municipality": "Rural Municipality"
};

const typeSuffixNe = {
  "Metropolitan City": "महानगरपालिका",
  "Sub Metropolitan City": "उपमहानगरपालिका",
  "Sub-Metropolitan City": "उपमहानगरपालिका",
  Municipality: "नगरपालिका",
  "Rural Municipality": "गाउँपालिका"
};

const makeWards = (n) => Array.from({ length: n }, (_, i) => String(i + 1));

const formatLocalEn = (name, type) => {
  const suffix = typeSuffixEn[type] || type || "";
  // Avoid double suffix if name already includes type
  if (new RegExp(suffix.replace(/\s+/g, "\\s+"), "i").test(name)) return name.trim();
  return `${name.trim()} ${suffix}`.trim();
};

const formatLocalNe = (nameNe, type) => {
  const suffix = typeSuffixNe[type] || "";
  const base = (nameNe || "").trim();
  if (!base) return suffix;
  if (suffix && base.includes(suffix)) return base;
  return suffix ? `${base} ${suffix}` : base;
};

let provinceCount = 0;
let districtCount = 0;
let localCount = 0;
const typeCounts = {};

const data = admin.provinces.map((province) => {
  provinceCount += 1;
  const mapped = provinceNameMap[province.name_en] || {
    en: province.name_en,
    np: province.name_ne
  };

  const districts = (province.districts || [])
    .slice()
    .sort((a, b) => a.name_en.localeCompare(b.name_en))
    .map((district) => {
      districtCount += 1;
      const children = (district.local_levels || [])
        .slice()
        .sort((a, b) => a.name_en.localeCompare(b.name_en))
        .map((ll) => {
          localCount += 1;
          const type = ll.type_en || "Municipality";
          typeCounts[type] = (typeCounts[type] || 0) + 1;
          const wards = makeWards(wardCountByType[type] || 9);
          return {
            en: formatLocalEn(ll.name_en, type),
            np: formatLocalNe(ll.name_ne, type),
            wards
          };
        });

      return {
        en: district.name_en,
        np: district.name_ne || district.name_en,
        children
      };
    });

  return {
    en: mapped.en,
    np: mapped.np || province.name_ne,
    children: districts
  };
});

// Sanity: every province should have districts; every district at least 1 local level
for (const p of data) {
  if (!p.children.length) throw new Error(`Province ${p.en} has no districts`);
  for (const d of p.children) {
    if (!d.children.length) throw new Error(`District ${d.en} has no local levels`);
  }
}

if (localCount !== 753) {
  console.warn(`Warning: expected 753 local levels, got ${localCount}`);
}

const outPath = path.join(__dirname, "../src/data/nepal-addresses.ts");
const header = `import type { NepalAddressProvince } from "../types.js";

/**
 * Complete Nepal address hierarchy for form selectors.
 * Source: official administrative structure — 7 provinces, 77 districts, 753 local levels
 * (6 Metropolitan, 11 Sub-Metropolitan, 276 Municipalities, 460 Rural Municipalities).
 * Regenerated by scripts/rebuild-nepal-addresses.mjs
 */
export const nepalAddressData: NepalAddressProvince[] = `;

const body = JSON.stringify(data, null, 2);
const file = `${header}${body};\n`;

fs.writeFileSync(outPath, file, "utf8");

console.log("Wrote", outPath);
console.log({
  provinceCount,
  districtCount,
  localCount,
  typeCounts,
  fileBytes: Buffer.byteLength(file, "utf8")
});

// Spot-check a few known rural municipalities users often miss
const checks = [
  ["Koshi Province", "Taplejung", "Sidingba Rural Municipality"],
  ["Bagmati Province", "Sindhupalchok", "Helambu Rural Municipality"],
  ["Gandaki Province", "Kaski", "Annapurna Rural Municipality"],
  ["Lumbini Province", "Rupandehi", "Mayadevi Rural Municipality"],
  ["Karnali Province", "Jumla", "Tila Rural Municipality"],
  ["Sudurpashchim Province", "Bajhang", "Talkot Rural Municipality"],
  ["Madhesh Province", "Rautahat", "Gujara Municipality"]
];

for (const [prov, dist, mun] of checks) {
  const p = data.find((x) => x.en === prov);
  const d = p?.children.find((x) => x.en === dist);
  const m = d?.children.find((x) => x.en === mun || x.en.includes(mun.replace(/ Rural Municipality| Municipality/g, "")));
  console.log(prov, dist, mun, "→", m ? "OK " + m.en : "MISSING");
}
